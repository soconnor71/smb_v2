LDR_Start:
		lda #$10	; Use 0x1000 for background (not that it matters, same in both)
		sta PPU_CTRL_REG1
		ldx #$ff 	; Reset stack pointer
		txs
		;
		; Wait for stable ppu state
		;
wait_vbl0:
		lda PPU_STATUS
		bpl wait_vbl0
wait_vbl1:
		lda PPU_STATUS
		bpl wait_vbl1
clear_memory:
		lda #$00
		sta $0000, x
		sta $0100, x
		sta $0300, x
		sta $0400, x
		sta $0500, x
		sta $0600, x
		sta $0700, x
		lda #$fe
		sta $0200, x
		inx
		bne clear_memory
		;
		; Install nametable
		;
		ldx PPU_STATUS	; Eead PPU status to reset the high/low latch
		ldx #$00
		stx PPU_SCROLL_REG ; No scrolling
		stx PPU_SCROLL_REG
		stx PPU_CTRL_REG2 ; No rendering
		;
		; Copycopycopycopy
		;
		lda PPU_STATUS
		lda #$20
		sta PPU_ADDRESS
		lda #$00
		sta PPU_ADDRESS
write_nt_0:
		lda nametable_data_0, x
		sta PPU_DATA
		inx
		bne write_nt_0
write_nt_1:
		lda nametable_data_1, x
		sta PPU_DATA
		inx
		bne write_nt_1
write_nt_2:
		lda nametable_data_2, x
		sta PPU_DATA
		inx
		bne write_nt_2
write_nt_3:
		lda nametable_data_3, x
		sta PPU_DATA
		inx
		bne write_nt_3
		;
		; Copy static sprite-data over
		;
		ldx #$00
copy_more_sprites:
		lda static_sprite_data, x
		sta $200, x
		inx
		bne copy_more_sprites
		;
		; Install palette
		;
		lda PPU_STATUS ; Latch
		lda #$3F
		sta PPU_ADDRESS
		ldx #$00
		stx PPU_ADDRESS
next_palette_entry:
		lda palette_data, x
		sta PPU_DATA
		inx
		cpx #$20
		bne next_palette_entry
		;
		; Enable sound
		;
		lda #$0f
		sta OperMode			; Anything but zero and it will play...
		sta SND_MASTERCTRL_REG
		lda #WaterMusic
		sta AreaMusicQueue
		;
		; Enable NMI
		;
		lda #$80
		sta PPU_CTRL_REG1
hang:
		jmp hang

